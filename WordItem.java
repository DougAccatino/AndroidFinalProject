public class WordItem {

    private int myID;
    private String myWord;


    public WordItem(){

    }
    public int getMyID(){
        return myID;
    }
    public String getMyWord(){
        return myWord;
    }
    public void setMyID(int id){
        myID = id;
    }
    public void setMyWord(String word){
        myWord = word;
    }

}